// Utility functions for hospital data
export function getHospitalsByState(state: string): Hospital[] {
  return HOSPITAL_DATABASE.filter((hospital) => hospital.state === state);
}

export function getHospitalsByDistrict(
  state: string,
  district: string
): Hospital[] {
  return HOSPITAL_DATABASE.filter(
    (hospital) => hospital.state === state && hospital.district === district
  );
}

export function getHospitalsByType(type: Hospital["type"]): Hospital[] {
  return HOSPITAL_DATABASE.filter((hospital) => hospital.type === type);
}

export function getEmergencyHospitals(state?: string): Hospital[] {
  return HOSPITAL_DATABASE.filter(
    (hospital) =>
      hospital.services.includes("EMERGENCY") &&
      hospital.isOpen24x7 &&
      (!state || hospital.state === state)
  );
}

export function getHospitalById(id: string): Hospital | undefined {
  return HOSPITAL_DATABASE.find((hospital) => hospital.id === id);
}

// Search hospitals within a radius (simplified)
export function getHospitalsNearCoordinates(
  coordinates: { latitude: number; longitude: number },
  radiusKm: number,
  state?: string
): Hospital[] {
  // This is a simplified implementation
  // In production, you'd use more sophisticated spatial indexing
  return HOSPITAL_DATABASE.filter((hospital) => {
    if (state && hospital.state !== state) return false;

    // Simple distance calculation (not using proper geospatial formula)
    const distance =
      Math.sqrt(
        Math.pow(hospital.coordinates.latitude - coordinates.latitude, 2) +
          Math.pow(hospital.coordinates.longitude - coordinates.longitude, 2)
      ) * 111; // Rough conversion to km

    return distance <= radiusKm;
  });
}
